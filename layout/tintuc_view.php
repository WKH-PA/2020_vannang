    <p>Ở tuổi 45, Dwayne Johnson đang là một trong những ngôi sao kiếm tiền giỏi nhất thế giới cho cả anh lẫn cho hãng phim. Tổng thu nhập mà cựu đô vật này có được nhờ đóng phim ước tính khoảng 220 triệu USD. Tổng doanh thu 27 bộ phim đã ra mắt của Dwayne mang về khoảng 9 tỉ USD (theo số liệu từ Boxoffice Mojo).</p>
    <p> Quan trọng hơn, hình ảnh và tên tuổi Dwayne Jonhson đã trở nên phổ biến với các khán giả đại chúng. Khi được Vanity Fair hỏi về bí quyết có được thành công ở cả hai sự nghiệp – đấu vật và diễn viên, Dwayne chân thành tiết lộ: &quot;Làm việc chăm chỉ và luôn biết ơn người khác. Bạn phải luôn tập trung và đừng mang cái tôi quá lớn. Khi đạt được thành công, đừng bao giờ tỏ ra tự mãn. Cuối cùng, đừng bao giờ nghi ngờ bản thân mình&quot;.</p>
    <p><img src="delete/banner_5.jpg" width="1200" height="774" /></p>
    <p>Nhìn lại quá khứ, con đường mà nhà cựu vô địch này đã đi không phải là một chiếc thảm trải đầy hoa hồng. Dawyne Douglas Johnson sinh ngày 2/5/1972, là thế hệ thứ ba trong một gia đình có truyền thống theo đuổi nghề đô vật. Bố anh là nhà vô địch Mỹ gốc Phi đầu tiên, còn ông ngoại anh – một tù trưởng lại là người Samoa đầu tiên chơi môn này.</p>
    <p>Nhưng ngay từ đầu, Dwayne Johnson đã không chọn con đường như cha và ông. Anh thích chơi bóng bầu dục, từng nhận được học bổng toàn phần của Đại học Miami để theo đuổi sự nghiệp cầu thủ. Giấc mơ tan vỡ vì một chấn thương quái ác. Và Dwayne quay trở về với đô vật – thứ đã luôn nằm trong máu thịt.</p>
    <p> Không được đào tạo diễn xuất bài bản nhưng lại nuôi tham vọng trở thành một Will Smith (phiên bản đô con hơn), nên Dwayne Johnson thường chọn những kịch bản có nhiều tính hành động, đậm chất giải trí.</p>
    <p>Bản thân anh từng chia sẻ: &quot;Tôi không thích phim có kết thúc buồn. Đời đã mang lại lắm thứ khốn khiếp – tôi không muốn nó xuất hiện trong phim của tôi đâu. Khi những dòng credit xuất hiện, tôi muốn cảm thấy mãn nguyện&quot;.</p>
    <p>Lớn lên trong một gia đình đô vật không phải là chuyện dễ dàng. Dwayne đã mô tả bố mình là một người cực kỳ khó tính. Chuyện bố thường xuyên đi thi đấu khiến cho gia đình ba người phải di chuyển rất nhiều. Dwayne đã sống ở 5 bang khác nhau lúc còn học mẫu giáo, lên trung học thì con số này ở mức 13.</p>
    <p> Năm 12, bố anh chuyển từ đấu tay đôi với đối thủ sang cãi nhau với mẹ anh. Bố thất nghiệp, gia đình lâm vào cảnh nghèo khổ, Dwayne bắt đầu ăn cắp rồi bị bắt. Anh đánh nhau nhiều hơn, trở thành một kẻ đầy giận dữ khi không thể kiểm soát cảm xúc của bản thân.</p>
    <p>Bố mẹ li dị. Năm 15 tuổi, Dwayne Johnson phải chứng kiến cảnh mẹ mình cố tự sát bằng cách đi thẫn thờ đi vào đường cao tốc sau khi họ bị đuổi khỏi căn hộ đang ở. Nỗi lo về mẹ, chấn thương, sự đổ vỡ với bạn gái khiến Dwayne phải đối mặt với vấn đề tâm lí trong nhiều năm. Nhưng thay vì bỏ cuộc, anh đã chọn chiến đấu và tự hàn gắn những vết thương lòng cho mình và cho cả mẹ.</p>
    <p>Chúng ta thường mỉa mai những kẻ to lớn, cục súc bằng câu ví von: &quot;Đầu óc ngu si, tứ chi phát triển&quot;. Nhưng với Dwayne Johnson, điều này lại chẳng đúng một chút nào.</p>
    <p>Hình thể đẹp, nụ cười tươi, gương mặt ngầu, khí chất điện ảnh và đặc biệt là những lựa chọn thông minh là thứ giúp cho cựu đô vật huyền thoại trở thành một ngôi sao màn bạc thực thụ.</p>

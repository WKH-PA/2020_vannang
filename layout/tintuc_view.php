    <p>Hollywood từng nhiều lần trao cơ hội diễn xuất cho các đấu sĩ chuyên nghiệp muốn &quot;đổi gió&quot; làm anh hùng màn ảnh, như John Cena, Dave Bautista, Kevin Nash… Nhưng có lẽ, chỉ có đô vật từng giành 17 chức vô địch The Rock là tạo ra được sự khác biệt lớn.</p>
    <p>Năm 2016, Dwayne Johnson – một kẻ tay ngang đã vượt mặt nhiều gương mặt gạo cội để trở thành Nam diễn viên được trả lương cao nhất thế giới với 64,5 triệu USD. Năm 2017, anh tụt xuống vị trí thứ hai trong danh sách với 65 triệu USD (thua kém người đứng đầu là Mark Wahlberg 3 triệu USD). Cũng trong năm này, hai trong số ba phim mà Dwayne Johnson tham gia đều đạt mức doanh thu khổng lồ: The Fate of the Furious càn quét với 1,163 tỉ USD còn Jumanji: Welcome to the Jungle âm thầm &quot;bỏ túi&quot; 944,4 triệu USD.</p>
    <p>Đạo diễn Brad Peyton (người từng cộng tác với Dwayne trong 3 phim Journey 2: The Mysterious Island, San Andreas và Rampage) đã gọi gã khổng lồ cao 1m96 này là &quot;Ngôi sao điện ảnh lớn nhất thế giới&quot;. Và có lẽ, lời khen này cũng không phải là sự tâng bốc quá lời.</p>
